"""Build the MEDIZO patent / tech documentation .docx.

Run:
    python scripts/build_patent_doc.py
Produces:
    docs/MEDIZO_Tech_Patent_Documentation.docx
"""

from __future__ import annotations

import os
from datetime import date
from pathlib import Path

from docx import Document
from docx.enum.style import WD_STYLE_TYPE
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.shared import Cm, Pt, RGBColor


REPO_ROOT = Path(__file__).resolve().parents[1]
OUT_PATH = REPO_ROOT / "docs" / "MEDIZO_Tech_Patent_Documentation.docx"


def configure_styles(doc: Document) -> None:
    styles = doc.styles

    normal = styles["Normal"]
    normal.font.name = "Calibri"
    normal.font.size = Pt(11)

    for level, size in [(1, 18), (2, 14), (3, 12)]:
        name = f"Heading {level}"
        h = styles[name]
        h.font.name = "Calibri"
        h.font.size = Pt(size)
        h.font.bold = True
        h.font.color.rgb = RGBColor(0x0B, 0x3D, 0x6E)

    title = styles["Title"]
    title.font.name = "Calibri"
    title.font.size = Pt(26)
    title.font.bold = True

    if "Code" not in [s.name for s in styles]:
        code = styles.add_style("Code", WD_STYLE_TYPE.PARAGRAPH)
        code.font.name = "Consolas"
        code.font.size = Pt(10)
        code.paragraph_format.left_indent = Cm(0.6)
        code.paragraph_format.space_before = Pt(4)
        code.paragraph_format.space_after = Pt(8)


def H1(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text, style="Heading 1")
    p.paragraph_format.space_before = Pt(18)
    p.paragraph_format.space_after = Pt(8)
    p.paragraph_format.keep_with_next = True


def H2(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text, style="Heading 2")
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.keep_with_next = True


def H3(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text, style="Heading 3")
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(4)
    p.paragraph_format.keep_with_next = True


def P(doc: Document, text: str) -> None:
    p = doc.add_paragraph(text)
    p.paragraph_format.space_after = Pt(6)


def BULLET(doc: Document, text: str) -> None:
    doc.add_paragraph(text, style="List Bullet")


def NUMBERED(doc: Document, text: str) -> None:
    doc.add_paragraph(text, style="List Number")


def CODE(doc: Document, text: str) -> None:
    doc.add_paragraph(text, style="Code")


def TABLE(doc: Document, header: list[str], rows: list[list[str]]) -> None:
    table = doc.add_table(rows=1 + len(rows), cols=len(header))
    table.style = "Light Grid Accent 1"
    for i, h in enumerate(header):
        cell = table.rows[0].cells[i]
        cell.text = h
        for r in cell.paragraphs[0].runs:
            r.bold = True
    for ri, row in enumerate(rows, start=1):
        for ci, val in enumerate(row):
            table.rows[ri].cells[ci].text = val
    doc.add_paragraph()


